// Getter and setter for player position
package game;

public class Jump4 {

    private int start;
    private int end;

    // Initializes jump with start and end positions
    public Jump4(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }
}